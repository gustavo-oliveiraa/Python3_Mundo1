num = int(input('Digite um número: '))
print('O dobro de {} vale {}.'.format(num, (num*2)))
print('O triplo de {} vale {}.'.format(num, (num*3)))
print('A raiz quadrada de {} é igual a {:.2f}.'.format(num, pow(num, (1/2))))

